#!/usr/bin/env node
// Trail secrets scanner — JS-regex-based replacement for the prior `grep -E`
// CI step (which used POSIX ERE and could not honor the redaction catalog's
// `\b` word-boundary anchors).
//
// Per PR #6 cycle-2 review N15 (P1 ci-correctness consensus HIGH):
//
//   The redaction-pattern catalog at bin/trail-redaction-patterns.yml uses
//   Perl-style word-boundary anchors (`\b`). POSIX ERE (`grep -E`) does NOT
//   support `\b`, so every `\b`-anchored pattern (most of the catalog) failed
//   to match in the CI job. The job ran green but matched nothing, making the
//   "secrets-scan" safety net illusory.
//
// This scanner consumes patterns through Node's regex engine (PCRE-compatible),
// which honors `\b`. Inputs come from stdin (a unified diff or arbitrary text)
// or from an explicit list of files passed via `--files`. The scanner exits
// non-zero on any match.
//
// Usage:
//   node bin/secrets-scan.mjs --diff <range>        # scan a git diff range
//   node bin/secrets-scan.mjs --files <path>...     # scan listed files
//   node bin/secrets-scan.mjs --self-test           # planted-secret self-test
//
// Patterns: bin/trail-redaction-patterns.yml (loaded relative to this script).
// Allowlist: paths matching ALLOWLIST_PATHS are excluded (test fixtures,
// the catalog itself, evidence files documenting prior detection).

import { execFileSync } from 'node:child_process';
import { readFileSync, writeFileSync, mkdtempSync, rmSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

// Why no `yaml` / `js-yaml` import: this script lives at the repo root and is
// invoked by the CI workflow. The pnpm workspace's `yaml` package is installed
// under apps/ui/node_modules, not at the repo root, so a Node resolver running
// from bin/ cannot reach it. Adding a root-level dep just for this one
// catalog file is heavier than parsing it ourselves — the catalog has a
// strictly regular shape (`- name: X\n  pattern: 'Y'\n  flags?: 'Z'`) that a
// focused parser handles deterministically. This mirrors how
// apps/ui/scripts/codegen-tokens.mjs hand-parses tokens.ts to avoid a tsx /
// esbuild dep.

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..');
const PATTERN_FILE = resolve(__dirname, 'trail-redaction-patterns.yml');

// Paths where matches are tolerated (test fixtures, the catalog itself,
// evidence files documenting prior detection, the secrets-scan tool itself,
// and deterministic dependency-resolution lockfiles whose integrity hashes
// structurally collide with the `high-entropy-string` pattern — see
// PR #6 cycle-3 review C3-N1, and gitleaks's analogous default exclusion
// at https://github.com/gitleaks/gitleaks for the same false-positive class).
const ALLOWLIST_PATHS = [
  /^bin\/trail-redaction-patterns\.yml$/,
  /^bin\/secrets-scan\.mjs$/,
  /^py-reference\/.*tests\/.*/,
  // Cycle-1.5 F7 (PR #21): the previous unscoped `\.test|\.spec` allowlists
  // silenced secrets-scan on EVERY .test.* / .spec.* file in the repo —
  // a future engineer pasting a real PAT into a copy-pasted reproduction
  // under any directory would slip through. Tighten to test directories
  // only (apps/*/tests/, apps/*/src-tauri/tests/, apps/*/test/ (Phase 1
  // singular), apps/*/test/fixtures/ (audit/capture redaction matrices),
  // py-reference/tests/ already covered above) so test-fixture noise is
  // silenced where it exists today, but new test files outside those
  // directories don't get blanket-allowlisted.
  //
  // Cycle-2 C1 (PR #21): F7 over-tightening missed the Phase 1 singular
  // `test/` convention used by apps/capture and apps/audit. The fix: add
  // both the test-file shapes AND the fixtures subdir. Without these,
  // existing fixtures (e.g., apps/capture/test/redaction-matrix.test.ts)
  // would surface as false positives in CI.
  /^apps\/[^/]+\/tests\/.*\.test\.(ts|tsx|py|mjs)$/,
  /^apps\/[^/]+\/tests\/.*\.spec\.(ts|tsx|py|mjs)$/,
  /^apps\/[^/]+\/src-tauri\/tests\/.*\.(ts|tsx|py|mjs|rs)$/,
  // Phase 1 singular `test/` convention (apps/capture, apps/audit):
  /^apps\/[^/]+\/test\/.*\.(test|spec)\.(ts|tsx|py|mjs)$/,
  /^apps\/[^/]+\/test\/fixtures\/.*/,
  // In-source unit tests colocated next to source files (TS pattern):
  /^apps\/[^/]+\/src\/.*\.test\.(ts|tsx|mjs)$/,
  /^apps\/[^/]+\/src\/.*\.spec\.(ts|tsx|mjs)$/,
  /^docs\/specs\//,
  /^\.claude\//,
  /^\.github\/workflows\//,
  // Lockfiles: integrity hashes / checksums are structurally identical to the
  // `high-entropy-string` pattern. Allowlisting is safe because lockfiles are
  // generated by package managers from registry-resolved content — they are
  // not a vector for human-authored secret leakage. Listed by exact basename
  // (rooted or any depth) to avoid accidentally allowlisting a developer's
  // file named `cargo.lock.bak` or similar.
  /^pnpm-lock\.yaml$/,
  /^package-lock\.json$/,
  /^yarn\.lock$/,
  /(?:^|\/)Cargo\.lock$/,
  // Font-integrity manifest: pinned SHA-256 hashes of vendored OFL fonts.
  // Same false-positive class as lockfile integrity hashes — generated from
  // public binary content, not human-authored secrets. Per gh#5 (Phase 4)
  // Newsreader vendoring; v0.2 backlog #77 will tighten the
  // high-entropy-string regex to exclude pure-hex digests generally.
  /^apps\/ui\/src\/design\/font-integrity\.json$/,
];

function isAllowlisted(path) {
  return ALLOWLIST_PATHS.some((re) => re.test(path));
}

// ---------------------------------------------------------------------------
// Pattern loading
// ---------------------------------------------------------------------------

/**
 * Parse the redaction-pattern catalog. The catalog format is strict:
 *
 *   - name: <ident>
 *     pattern: '<regex>'
 *     flags: '<imsx-subset>'   # optional
 *
 * with optional comment lines starting with `#` and blank lines between
 * entries. Single-quoted YAML strings escape only one form: `''` for a
 * literal `'`. We honor that escape; everything else is a literal char.
 */
function unescapeSingleQuoted(s) {
  // YAML 1.2 single-quoted: only `''` is an escape for a single `'`.
  return s.replace(/''/g, "'");
}

function parseCatalog(text) {
  const lines = text.split(/\r?\n/);
  const entries = [];
  let current = null;

  const valueRe = (key) =>
    new RegExp(`^\\s+${key}:\\s+'((?:[^']|'')*)'\\s*$`);
  const nameRe = /^\s*-\s*name:\s*([A-Za-z0-9_-]+)\s*$/;
  const patternRe = valueRe('pattern');
  const flagsRe = valueRe('flags');

  for (const line of lines) {
    if (/^\s*#/.test(line) || /^\s*$/.test(line)) continue;
    let m;
    if ((m = nameRe.exec(line))) {
      if (current) entries.push(current);
      current = { name: m[1] };
      continue;
    }
    if (current && (m = patternRe.exec(line))) {
      current.pattern = unescapeSingleQuoted(m[1]);
      continue;
    }
    if (current && (m = flagsRe.exec(line))) {
      current.flags = unescapeSingleQuoted(m[1]);
      continue;
    }
    // Top-level scalars (`version: ...`) and `patterns:` header are fine to
    // skip silently. Anything else inside an entry context that we don't
    // recognize is also non-fatal — extra fields would be additive.
  }
  if (current) entries.push(current);
  return entries;
}

function loadPatterns() {
  const text = readFileSync(PATTERN_FILE, 'utf8');
  const entries = parseCatalog(text);
  if (entries.length === 0) {
    throw new Error(`pattern catalog parsed empty: ${PATTERN_FILE}`);
  }
  const out = [];
  for (const entry of entries) {
    if (typeof entry?.name !== 'string' || typeof entry?.pattern !== 'string') {
      throw new Error(`pattern entry malformed: ${JSON.stringify(entry)}`);
    }
    // Convert YAML 'flags' string (subset of 'imsx') to JS RegExp flag string.
    // JS supports i, m, s; 'x' (extended/whitespace-ignore) is not a JS flag —
    // strip it and document the gap. (Trail's catalog v0.1.3 doesn't use 'x'
    // anywhere; this is forward-compatibility.)
    let flags = entry.flags ?? '';
    flags = flags.replace(/x/g, '');
    let re;
    try {
      re = new RegExp(entry.pattern, flags + 'g');
    } catch (err) {
      throw new Error(
        `pattern '${entry.name}' failed to compile in JS regex engine: ${err.message}`,
      );
    }
    out.push({ name: entry.name, regex: re });
  }
  return out;
}

// ---------------------------------------------------------------------------
// Scan logic
// ---------------------------------------------------------------------------

/**
 * Scan a chunk of text for any pattern hits. Returns an array of
 * { name, match, lineNo, line } findings.
 */
function scanText(text, patterns) {
  const findings = [];
  const lines = text.split(/\r?\n/);
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    for (const { name, regex } of patterns) {
      // Reset lastIndex; we want all matches per line.
      regex.lastIndex = 0;
      let m;
      while ((m = regex.exec(line)) !== null) {
        findings.push({ name, match: m[0], lineNo: i + 1, line });
        if (m.index === regex.lastIndex) regex.lastIndex++;
      }
    }
  }
  return findings;
}

/**
 * Scan a unified-diff blob: only added lines (lines starting with `+` but not
 * the `+++` file marker) are considered. Returns findings annotated with the
 * file path inferred from the most recent `+++ b/<path>` header.
 */
function scanDiff(diffText, patterns) {
  const findings = [];
  const lines = diffText.split(/\r?\n/);
  let currentPath = null;
  for (const line of lines) {
    if (line.startsWith('+++ ')) {
      // `+++ b/path/to/file` or `+++ /dev/null`
      const m = /^\+\+\+ (?:b\/)?(.+)$/.exec(line);
      currentPath = m ? m[1] : null;
      continue;
    }
    if (line.startsWith('--- ') || line.startsWith('@@') || line.startsWith('diff ')) {
      continue;
    }
    if (!line.startsWith('+')) continue;
    // The leading `+` is part of the diff format; strip it before scanning.
    const content = line.slice(1);
    if (currentPath && isAllowlisted(currentPath)) continue;
    for (const { name, regex } of patterns) {
      regex.lastIndex = 0;
      let m;
      while ((m = regex.exec(content)) !== null) {
        findings.push({
          name,
          match: m[0],
          file: currentPath ?? '<unknown>',
          line: content,
        });
        if (m.index === regex.lastIndex) regex.lastIndex++;
      }
    }
  }
  return findings;
}

// ---------------------------------------------------------------------------
// Entry points
// ---------------------------------------------------------------------------

function runDiff(range) {
  const diff = execFileSync('git', ['diff', range], {
    cwd: REPO_ROOT,
    encoding: 'utf8',
    maxBuffer: 64 * 1024 * 1024,
  });
  const patterns = loadPatterns();
  return scanDiff(diff, patterns);
}

// Cycle-2 C11 (PR #21): tool error vs. secrets-found are distinct exit codes.
// Used by runFiles when an input path either escapes REPO_ROOT (path traversal
// guard) or fails to read for any other reason. Exit 2 mirrors the convention
// already used by `--diff` missing-range and `--files` empty-list paths.
class ToolError extends Error {
  constructor(message) {
    super(message);
    this.name = 'ToolError';
  }
}

function runFiles(paths) {
  const patterns = loadPatterns();
  const findings = [];
  // Anchor with trailing separator so "../REPO_ROOT_sibling/foo" cannot
  // satisfy startsWith(REPO_ROOT) by prefix-collision.
  const repoPrefix = REPO_ROOT.endsWith('/') ? REPO_ROOT : REPO_ROOT + '/';
  for (const path of paths) {
    if (isAllowlisted(path)) continue;
    const abs = resolve(REPO_ROOT, path);
    if (!(abs === REPO_ROOT || abs.startsWith(repoPrefix))) {
      throw new ToolError(
        `path escapes repo root: '${path}' resolves to '${abs}' (REPO_ROOT='${REPO_ROOT}')`,
      );
    }
    let text;
    try {
      text = readFileSync(abs, 'utf8');
    } catch (err) {
      throw new ToolError(`cannot read '${path}': ${err.message}`);
    }
    for (const f of scanText(text, patterns)) {
      findings.push({ ...f, file: path });
    }
  }
  return findings;
}

/**
 * Self-test: write a fixture file containing known secret shapes, scan it,
 * assert the scanner finds them all, then clean up. Exit non-zero on any
 * miss. This is the "planted-secret test" called for in cycle-2 N15: it
 * proves the scanner actually catches secrets.
 */
function runSelfTest() {
  // Each entry: { pattern_name, planted_secret }. The shapes match the
  // catalog's regexes; the secrets themselves are synthetic (do NOT contain
  // real credentials).
  const planted = [
    { name: 'aws-access-key', secret: 'AKIAABCDEFGHIJKLMNOP' },
    { name: 'github-token', secret: 'ghp_aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa' },
    {
      // Catalog regex requires exactly 82 [A-Za-z0-9_] chars after the
      // `github_pat_` prefix. Off-by-one would break the bound check; this
      // fixture is intentionally exact.
      name: 'github-fine-grained-token',
      secret:
        'github_pat_' +
        'a'.repeat(22) + '_' + 'b'.repeat(59), // 22 + 1 + 59 = 82
    },
    { name: 'openai-api-key', secret: 'sk-aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa' },
    { name: 'slack-token', secret: 'xoxb-1234567890ab' },
    { name: 'google-api-key', secret: 'AIzaABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghi' },
    { name: 'npm-token', secret: 'npm_aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa' },
    { name: 'jwt', secret: 'eyJhbGciOi.eyJzdWIi.SflKxwRJSMeKKF3OglQ==' },
    { name: 'postgres-url', secret: 'postgres://user:pass@db.example/foo' },
  ];

  const tmp = mkdtempSync(join(tmpdir(), 'trail-secrets-selftest-'));
  const fixturePath = join(tmp, 'fixture.txt');
  // Write each planted secret on its own line. The file path is OUTSIDE
  // the allowlist so all matches will register.
  const body =
    '# fixture: planted secrets for scanner self-test\n' +
    planted.map((p) => `# ${p.name}\n${p.secret}\n`).join('') +
    '# end fixture\n';
  writeFileSync(fixturePath, body, 'utf8');

  try {
    const patterns = loadPatterns();
    const text = readFileSync(fixturePath, 'utf8');
    const found = scanText(text, patterns);

    const foundNames = new Set(found.map((f) => f.name));
    const missing = planted.filter((p) => !foundNames.has(p.name));

    if (missing.length > 0) {
      console.error('SELF-TEST FAILED — patterns did not match planted secrets:');
      for (const m of missing) {
        console.error(`  - ${m.name}: '${m.secret}'`);
      }
      console.error('\nFound matches:');
      for (const f of found) {
        console.error(`  - ${f.name}: '${f.match}'`);
      }
      return 1;
    }

    console.log(
      `SELF-TEST PASSED — all ${planted.length} planted secrets matched ` +
        `(${found.length} total hits across ${foundNames.size} unique patterns).`,
    );
    return 0;
  } finally {
    rmSync(tmp, { recursive: true, force: true });
  }
}

function printFindings(findings) {
  for (const f of findings) {
    const where = f.file ? `${f.file}` : '<text>';
    const lineRef = f.lineNo ? `:${f.lineNo}` : '';
    console.error(`::error file=${where}${lineRef}::secret pattern '${f.name}' matched: ${f.match}`);
  }
}

// ---------------------------------------------------------------------------
// CLI
// ---------------------------------------------------------------------------

function main() {
  const argv = process.argv.slice(2);
  if (argv.length === 0 || argv[0] === '--help' || argv[0] === '-h') {
    console.log(
      'usage: secrets-scan.mjs (--diff <range> | --files <path...> | --self-test)',
    );
    return 0;
  }

  if (argv[0] === '--self-test') {
    return runSelfTest();
  }

  if (argv[0] === '--diff') {
    const range = argv[1];
    if (!range) {
      console.error('--diff requires a range argument (e.g., origin/main...HEAD).');
      return 2;
    }
    const findings = runDiff(range);
    if (findings.length > 0) {
      printFindings(findings);
      console.error(`secrets-scan: ${findings.length} match(es) in diff; redact and re-push.`);
      return 1;
    }
    console.log('secrets-scan: no redaction-pattern matches in the diff.');
    return 0;
  }

  if (argv[0] === '--files') {
    const paths = argv.slice(1);
    if (paths.length === 0) {
      console.error('--files requires at least one path.');
      return 2;
    }
    let findings;
    try {
      findings = runFiles(paths);
    } catch (err) {
      // Cycle-2 C11 (PR #21): exit 2 distinguishes "tool error"
      // (path traversal, unreadable file) from exit 1 ("secrets found").
      // Callers (CI, pre-commit hooks) gate on != 0 today; the split lets
      // a future hook differentiate "redact and re-push" from "fix your
      // invocation". The only `--files` caller in CI is the diff-driven
      // workflow, which always passes repo-root-relative paths.
      if (err instanceof ToolError) {
        console.error(`secrets-scan: tool error: ${err.message}`);
        return 2;
      }
      throw err;
    }
    if (findings.length > 0) {
      printFindings(findings);
      console.error(`secrets-scan: ${findings.length} match(es) across ${paths.length} file(s).`);
      return 1;
    }
    console.log(`secrets-scan: no redaction-pattern matches across ${paths.length} file(s).`);
    return 0;
  }

  console.error(`unknown argument: ${argv[0]}`);
  return 2;
}

process.exit(main());
